// Displays the value of two variables
#include <stdio.h>

int main(void)
{
    // Decleare a variable x as an integer, assign 42 to it
    int x = 42;

    // Declare a variable y as an integer, assign x + 1 to it
    int y = x + 1;

    // Display the value of x
    printf("x is %i\n", x);

    // Display the value of y
    printf("y is %i\n", y);

}