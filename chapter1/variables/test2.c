// Displays the value of two variables
#include <stdio.h>

int main(void)
{
    // Decleare a variable x as an integer, assign 42 to it
    int x = 42;

    // Declare a variable y as an integer, assign x + 1 to it
    int y = x + 1;

    // Display the current values of both x and y
    printf("The value of x is %i and the value of y is %i\n", x, y);

}